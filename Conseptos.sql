

variables de shell (o locales): {
    Son variables que solo existen dentro de la sesión de shell actual y solo son accesibles
     por el shell mismo. No se pasan a los procesos hijos.
}
Variables de entorno: {
    Son variables que, además de ser accesibles por el shell actual, también son accesibles 
    por cualquier proceso hijo que se inicie desde ese shell.
}
Globbing{
    caracteres de globbing se denominan a menudo como "comodines". Estos son símbolos que tienen un significado especial para el shell
}
*{
    El asterisco se utiliza para representar cero o más de cualquier carácter en un nombre de archivo.
    
}